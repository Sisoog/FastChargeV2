/*
 * Name         : writeCharBig
 * Description  : Write a single big character to screen
 * 		  Note: bigfont only includes digits, '.', '+', '-'
 * Argument(s)  : x,y - starting position on screen, x=0-83, y=0-6
 *                ch - character to display
 *                mode - reverse or normal
 * Return value : none
 */
void GBwriteCharBig (unsigned char x,unsigned char y,unsigned char ch)
{
unsigned char i, j;
unsigned char *pFont;
unsigned char ch_dat;
unsigned char colsUsed = 12;

   pFont = (unsigned char *) big_number;

   if(ch == '.')
   {
     ch = 10;
     colsUsed=5;
   }
   else if (ch == '+')
     ch = 11;
   else if (ch == '-')
     ch = 12;
   else
     ch = ch & 0x0f;


   for(i=0;i<3;i++)
   {
     //gotoXY( x, y+i);

     for(j=0; j<colsUsed; j++)
     {
       ch_dat =  *(pFont+ch*48 + i*16 +j);	// 16 cols in data for char even if we use fewer.
       //writeData( ch_dat);
       GBGWrite(x+j,y+(i*8),8,ch_dat);
     }
   }
}
